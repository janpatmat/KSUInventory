    private void deactivateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deactivateBtnActionPerformed
//       printThis();
        if (deacTable.getSelectedRowCount() > 0){
            int yesNO = 0;
            yesNO = JOptionPane.showConfirmDialog (null, "Do you really want to reactivate these products?","Warning",JOptionPane.YES_NO_OPTION);
            if (yesNO == JOptionPane.YES_OPTION){

                int[] allSelected = deacTable.getSelectedRows();
                for (int i: allSelected){

                    int id = Integer.parseInt(deacTable.getValueAt(i, 0).toString());

                    try{
                        st = KsuFinal.con.prepareStatement("UPDATE producttable SET Active = 'TRUE' WHERE productID = " + id);
                        st.executeUpdate();
                    }
                    catch(Exception e){
                        System.out.println(e);
                    }
                }
                EditProduct.editActiveTable();
                editDeactiveTable();
            }
        }else{
            JOptionPane.showMessageDialog(this,"Please select a row", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deactivateBtnActionPerformed

