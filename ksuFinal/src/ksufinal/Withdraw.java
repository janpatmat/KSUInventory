    private void withdrawBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawBtnActionPerformed
        
        
        if (quanAndUnitArr.size() > 0){
            int idx = jComboBox1.getSelectedIndex();
            int quan = Integer.parseInt(quanAndUnitArr.get(idx)[0]);
            int inputQuan = Integer.parseInt(withdrawQuanTF.getText());
            
            if (inputQuan < quan){
                // do action
                String newQuan = String.valueOf(quan - inputQuan);
                
                String statement = "UPDATE expenses.product SET Quantity = " + newQuan + " WHERE Name = '" + jComboBox1.getSelectedItem().toString() + "'";
                            
                
                try{
                    KsuFinal.con.prepareStatement(statement).executeUpdate();
                    
                }
                catch(Exception e){
                    System.out.println(e);
                }
                
                
            }
            else{
                JOptionPane.showMessageDialog(this,"You cannot withdraw this kind of quantity");
            }
        }
        
        
        
    
    
    
    }//GEN-LAST:event_withdrawBtnActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            rs = KsuFinal.con.createStatement().executeQuery("SELECT * FROM expenses.product;");
            while(rs.next()){

                String nm = rs.getString("Name");
                String qt = rs.getString("Quantity");
                String ut = rs.getString("Unit");
                
                jComboBox1.addItem(nm);
                String[] item = {qt, ut};
                
                quanAndUnitArr.add(item);
                
            }  
        }
        catch(Exception e){
            System.out.println(e);
        }
        
        int idx = jComboBox1.getSelectedIndex();
        String quan = quanAndUnitArr.get(idx)[0];
        String unit = quanAndUnitArr.get(idx)[1];
        
        
        
        quantityShow.setText("Quantity in the Inverntory: " + quan + " " + unit);
        unitShow.setText(unit);
        
    }//GEN-LAST:event_formWindowOpened

